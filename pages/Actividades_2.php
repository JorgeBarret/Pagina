<h4>Conecpto de seguridad informática</h4><h5>La seguridad informática es una disciplina que se relaciona a diversas técnicas, aplicaciones y dispositivos encargados de asegurar la integridad y privacidad de la información de un sistema informático y sus usuarios.


Técnicamente es imposible lograr un sistema informático ciento por ciento seguro, pero buenas medidas de seguridad evitan daños y problemas que pueden ocasionar intrusos.


Seguridad con respecto a la naturaleza de la amenaza


Existen dos tipos de seguridad con respecto a la naturaleza de la amenaza:
* Seguridad lógica: aplicaciones para seguridad, herramientas informáticas, etc.
* Seguridad física: mantenimiento eléctrico, anti-incendio, humedad, etc.</h5>
			  <h4>¿Cuáles son los objetivos de la misma?</h4><h5>Busca garantizar que los recursos de un sistema de información sean utilizados tal como una organización o un usuario lo ha decidido, protegiendo al mismo de las amenazas que se puedan ocasionar, evitando intromisiones.
Tiene 5 objetivos principales:
Integridad: garantizar que los datos sean los que se supone que son.
Confidencialidad: asegurar que sólo los individuos autorizados tengan acceso a los recursos que se intercambian.
Disponibilidad: garantizar el correcto funcionamiento de los sistemas de información.
Evitar el rechazo: garantizar de que no pueda negar una operación realizada.
Autenticación: asegurar que sólo los individuos autorizados tengan acceso a los recursos</h5>
			  <h4>¿Cuáles y de que tipo son las amenazas?
</h4><h5>Amenazas a un sistema informático
Existen diferente tipos:
* Programas malignos: virus, espías, troyanos, gusanos, phishing, spamming, etc.


* Siniestros: robos, incendio, humedad, etc. pueden provocar pérdida de información.


* Intrusos: piratas informáticos pueden acceder remotamente (si está conectado a una red) o físicamente a un sistema para provocar daños.


* Operadores: los propios operadores de un sistema pueden debilitar y ser amenaza a la seguridad de un sistema no sólo por boicot, también por falta de capacitación o de interés.</h5>
			  <h4>¿Qué se entiende por hacker y cracker?</h4><h5>Los hackers son los buenos (los que programan el softwareque utilizamos) y los crackers lo malos (aquellos que se encargan de vulnerar los sistemas que crean los hackers).La diferencia clave entre un hacker y un cracker es que el cracker vulnera el software/sistema que el hacker crea. Por lo tanto, un hacker y un cracker son dos personas, si bien, con conocimientos similares, pero con ideas completamente diferentes. En los hackers suele existir un código de ética, contrario a los crackers que se valen de cualquier medio para lograr su objetivo.</h5>
			 

